                <h2>Nyungwe National Park</h2>
                <div>
                    Nyungwe National Park is found in western Rwanda and is one of the worlds most beautiful and pristine rain forests. It is dominated by a series of mountains, originating in northern Uganda and Congo, running south through the Rwenzori mountains, western Rwanda and Burundi, to the shores of Lake Tanganyika.
                </div>
                <div>
                    Nyungwe National Park offers scenic views over the Lake Kivu and sometimes the Virunga ranges on a clear day. The forest has an extensive network of well-maintained walking and hiking trails to various waterfalls and viewing points. The prime activities in Nyungwe forest are chimp trekking, bird watching, colobus monkey trekking and the canopy walk.
                </div>
                <h2>Wildlife in Nyungwe National Park</h2>
                <h3>Animals in Nyungwe National Park</h3>
                <div>
                    Nyungwe forest is home to 75 different species of mammals with most of these being small and or nocturnal making it difficult for sighting. Chimpanzees are the main attraction in Nyungwe  with chimp trekking happening daily early morning. The forest is home to troops of 300 colobus monkeys or more roaming in the trees, giving visitors to the forest another engaging activity.
                </div>
                <h3>Nyungwe forest’s 13 recorded primate species</h3>
                <ul class="custom-list">
                    <li>Chimpanzees</li>
                    <li>Silver monkey</li>
                    <li>Golden monkey</li>
                    <li>Owl-faced monkey/ Hamlyn’s monkey</li>
                    <li>Grey-cheeked mangabey</li>
                    <li>Red-tailed monkey</li>
                    <li>L’Hoest’s monkey</li>
                    <li>Vervet monkey</li>
                    <li>Olive baboon</li>
                    <li>Dent’s Mona Monkey</li>
                    <li>Blue Monkey</li>
                    <li>Angolan Colobus Monkeys</li>
                </ul>            
                <h3 style="margin: 1rem 0 0 0;">Birds in Nyungwe National Park</h3>
                <div>
                    Nyungwe forest is home to about 300 species of birds with 16 endemics, 27 Albertine rift endemics, and 121 forest species. The bird species include; Chestnut Owlet, Olive and Elliot’s Woodpeckers, Great Blue and Ruwenzori Turacos, White-headed Wood-hoopoe, Barred Long-tailed Cuckoo, , Stripe-breasted Tit, the remarkable Red-collared Babbler, Archer’s Robin Chat, White-bellied Robin Chat, Doherty’s Bush-Shrike, Handsome Francolin Mountain Masked, Black-faced, Chapin’s Flycatcher, Purple-breasted and Regal Sun-birds, Chestnut-throated, Collared Apalis, Rwenzori Batis, Neumann’s Short-tailed Warbler, White-tailed Blue-flycatcher, etc.
                </div>
                <h2 style="margin: 1rem 0 0 0;">Activities in Nyungwe National Park</h2>
                <h3 style="margin: 1rem 0 0 0;">Chimpanzee Trekking in Nyungwe National Park</h3>
                <div>
                    Nyungwe forest is home to two wild chimp communities that are habituated for chimpanzee tourism. One of the groups is in Cyamudongo section of Nyungwe forest and the other around the Uwinka area of the forest. The forest is home to about 400 Chimpanzees. While viewing chimpanzees is not guaranteed, you will enjoy learning about these creatures and their natural habitats from your guides. You are expected to be at the briefing area as early as 5.00 am in the morning. This is because chimps are mostly likely to be seen early in the morning and they tend to move about which requires one to be active as you follow after them. However whether you view the chimps or not, the experience in the forest is rich as you watch other primates, butterflies, several trees and above all the freshness of the air.
                </div>
                <h3>Birdwatching in Nyungwe National Park</h3>
                <div>
                    Nyungwe forest national park is a high altitude mountain forest ranging between 1600 – 2950 meters. The birds in Nyungwe forest are mostly found in the trees, shrubs or the ground at the edges of the forest next to the tea plantations. Some birds are shy and therefore more easily heard than seen like many forest birds. Among the frequently sighted species are the Rwenzori Nightjar, White-tailed Crested Flycatcher, Kivu Ground Thrush, the Handsome Francolin, Dwarf Honeyguide, Greatblue and Rwenzori Turaco, Montane Double Collared Sun and the Red- Throated Alethe. There are well maintained trails which you will take while birding as well as trained bird guides that are based at the nyungwe forest.
                </div>
                <h3>Nature and hiking walks in Nyungwe National Park</h3>
                <div>
                    Nyungwe NP has steep landscape and extensive network of hiking trails making it a perfect destination for hikers. The trails are different in length, ease of hiking and features; you are therefore advised to ask the ranger guides for advice on which trail to take according to the attractions you are seeking. The trails are through a diverse landscape and represent the most scenic way of exploring the forest. Below are some of the trails you can choose to take on while hiking in the forest
                </div>
                <div class="trail">
                    <h3>Congo – Nile divide trail</h3><p>  is a 227 km trek for experienced hikers which lasts several days to hike through. However one can choose an option for one-day hike of part of the section. The main attraction on this trail is the varied landscapes and communities along the route. There are campsites where you will overnight but you do have to bring your own camping equipment. As for basic supplies, there are shops along the trail where you can purchase these.</p>
                </div>
                <div class="trail">
                    <h3>The Igishigishigi trail</h3><p>  covers a distance of 2km with remarkable views of the Nyungwe forest canopy, beautiful wild flowers, occasional monkeys and several birds. The total time on this trail is between one and two hours depending on ones interests. All participants must be at least six years of age and adults must acompany children.</p>
                </div>
                <div class="trail">
                    <h3>Karamba trail</h3><p> is a good birding spot in Nyungwe, which is flanked by numerous fern trees located on the former gold mine site, market and army camp.</p>
                </div>
                <div class="trail">
                    <h3>Umugote trail</h3><p>is great for primate viewing and bird watching in Nyungwe forest with views of ridges, flowers, and butterflies. On a clear day you can see Kibira national park in Burundi.</p>
                </div>
                <div class="trail">
                    <h3>Umoyove trail</h3><p>also known as Mahogany trail takes you to the waterfall for a relaxed time as you stroll by the parks tallest trees.</p>
                </div>
                <div class="trail">
                    <h3>Uwinka trail</h3><p>is a difficult hike but a beautiful place for bird watching.</p>
                </div>
                <div class="trail">
                    <h3>Irebero trail</h3><p>This trail provide views of Lake Kivu and two of the highest peaks in Nyungwe national park.</p>
                </div>
                <div class="trail">
                    <h3>Imbaraga trail</h3><p>is 10km geared towards experienced hikers. Hikers will have views of waterfalls, a refreshing sight after a long hike.</p>
                </div>
                <div class="trail">
                    <h3>Bigugu Trail</h3><p>provides views of the DR Congo and Lake Kivu as well as the Islands. It takes you to the highest peak in nyungwe forest park.</p>
                </div>
                <div class="trail">
                    <h3>Isumo Trail</h3><p>will get you to experience the beautiful tea fields – you can even plan to get involved in picking tea and follow through the entire process until it gets to your cup. You will also enjoy the view of the biggest waterfall in Nyungwe national park.</p>
                </div>
                <div class="trail">
                    <h3>Kamiranzovu Trail</h3><p>brings you close to most of the waterfalls, wetland and swamps.</p>
                </div>
                <div class="trail">
                    <h3>Muzimu Trail</h3><p>takes you through eucalyptus and pine forests.</p>
                </div>
                <div class="trail">
                    <h3>Ngabwe Trail</h3><p>leads to a lovely picnic and camping site</p>
                </div>
                <div class="trail">
                    <h3>Rukuzi Trail</h3><p> takes you through chimpanzee spotting locations and village views</p>
                </div>
                <h2>Canopy Walk in Nyungwe forest</h2>
                <div>The canopy walk in Nyungwe forest is along the Igishigishigi trail. The walkway is accessible as part of a specialized guided tour along the trail. The canopy walk hangs 60 meters above the forest floor between giant trees and towers, which provides a stunning view of the park.</div>
                <h2> Cultural tours in Nyungwe forest national park</h2>
                <div>To complete your experience of Nyungwe forest, add on your itinerary a visit to the community to engage in the rich Rwandan culture. The communities along the boundaries of this forest have co-existed with it and helped to some extent in its preservation after understanding its importance to the Rwanda’s eco system. There has been efforts to launch community-based experiences to preserve the culture of Rwanda and offer the visitors a rich experience. Once at the village you can be part of activities such as a dance ceremony, banana beer production, and bow-and-target shooting,  traditional medicine making and maize-grinding etc.</div>
                <h2>How to get to Nyungwe National Park</h2>
                <div>Drive from Kigali to Nyanza to Huye and finally Nyungwe National Park. The drive takes approximately 5 hours depending on your stopovers.</div>
                <div>Alternatively you can fly using the services of Akagera Aviation.</div>
