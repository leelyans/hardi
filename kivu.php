                <h2>Lake Kivu</h2>
                <div>Shared between Rwanda and the DR Congo, beautiful Lake Kivu extends for 2,370km² across the 
                    floor of the Albertine Rift, and hemmed in by a steediv terraced escarpment that rises up to 
                    1.5km above its surface. Ranked among the world’s 20 deepest and 20 most voluminous freshwater 
                    bodies, it is lined with pretty fishing villages and a trio of larger ports in the form of Rusizi,
                    Karongi and Rubavu. The latter in particular has long served as a popular weekend retreat for residents 
                    of Kigali, and as the most northerly point on the Rwandan lakeshore, it also forms a great place to chill 
                    out after tracking gorillas in nearby Volcanoes National Park.
                </div>
                <div>
                    The most southerly port on Lake Kivu, Rusizi (formerly Cyangugu) is split into a bland upper town perched 150m above the water and a smaller but more atmospheric lower town set alongside the Rusizi River as it exits the lake. A short ride on a local amato boat (comprising three dugouts bound together with bamboo poles) leads to the islands of Gihaya and Nkombo, both of which support an abundant birdlife.
                </div>
                <div>
                    The closest Kivu port to Kigali, Karongi (formerly Kibuye) sprawls prettily across a row of pine- and eucalyptus-planted hills interwoven with the arms of the lake. It is notable for its bustling Saturday market and the recently-opened Museum of the Environment, which incorporates a collection of stuffed animals and mounted butterflies, and s rooftop garden of well-labelled plants used as traditional medicine. Nearby Napoleon’s Island is shaped like its namesake’s hat and supports a massive colony fruit bats.
                </div>
                <div>
                    Situated at an altitude of 2,150m on the escarpment above the lakeshore, the 100m-high Chutes de Ndaba is most impressive in the rainy season and a rough footpath leads to the base from the main road between Karongi road and Rubavu
                </div>
                <div>
                    Rwanda’s newest conservation area, gazetted in 2016, the 35km² Gishwati-Mukura National Park protects two relict patches of the vast tract of montane rainforest that once covered the Rift Valley escarpment south of Nyungwe. The more interesting to tourists is the Gishwati Forest, which supports significant populations of chimpanzee, L’Hoest’s monkey and golden monkey, along with 120 bird species, including Albertine Rift endemics such as Ruwenzori turaco, strange weaver and red-throated alethe. the park is not yet open to tourists but a network of walking trails is planned.
                    The most popular Kivu port with tourists is the leafy town of Rubavu (formerly Gisenyi), which lies just 60km from the gorilla tracking facilities at Volcanoes National Park. Rubavu’s lovely setting is capped by by the distinctive outline of Nyiragongo, the most active of the Virunga volcanoes, which lies on the opposite side of the Congolese border, and often belches out smoke by day and glows ominously at night. Tourist facilities are concentred 6km out of town at Rubona, a scenic lakeside village notable for its pretty beaches and fishing harbour.
                </div>
                <div>
                    The scenic Congo-Nile Trail runs roughly parallel to the eastern shore of Lake Kivu between Rubavu and Rusizi along 227km of existing roads and motorable tracks. Divided into ten stages, it takes ten days to complete on foot, five by bicycle and two or three by 4x4, sleeping at various lakeshore villages and towns.
                </div>
                <h2 style="margin: 2rem 0 0 0;">Practical advice</h2>
                <li>
                    RwandAir operates a once-daily flight between Kigali and Kamembe airport outside Rusizi (formerly Cyangugu). Otherwise, main ports on Lake Kivu are most easily reached by road. Rusizi is about 220km/5 hours southwest of Kigali via Huye and Nyungwe National Park, whilst Karongi is 135km/2 hours west of Kigali and Rubavu is 155km/3 hours northwest via Musanze (the gateway town for gorilla tracking in Volcanoes National Park). A newly surfaced 200km road also connects the three main ports to each other, as does a twice-weekly ferry service.
                </li>
                <li>
                    A decent selection of budget and midrange accommodation is available at Rusizi, Karongi or Rubavu. For something more upmarket, you are best heading to Rubavu. Boat trips on the lake can be arranged at all the ports.
                </li>
